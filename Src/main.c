int main(void)
{

  /* USER CODE BEGIN 1 */

  // we have to clean up after the bootloader first
  {
    // it leaves all the interrupts enabled
    for (IRQn_Type interrupt=0; interrupt<=56; interrupt++)
      HAL_NVIC_DisableIRQ(interrupt);
    // and all the peripherals turned on
    HAL_DeInit();
  }

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_LCD_Init();
  MX_SDIO_SD_Init();
  MX_FATFS_Init();
  MX_USART2_UART_Init();
  MX_ADC_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_RTC_Init();

  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
    
    LCD_SEGON(SEG_ICON_DANGER);
    lcd_update();
    HAL_Delay(500);

    LCD_SEGOFF(SEG_ICON_DANGER);
    lcd_update();
    HAL_Delay(500);

  }
  /* USER CODE END 3 */

}

